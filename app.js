document.addEventListener("DOMContentLoaded", () => {
  const languageSelector = document.getElementById("language-selector");
  const dropdownMenu = document.getElementById("language-dropdown");
  const languageOptions = document.querySelectorAll(".lang-option");
  const currentLangText = document.getElementById("current-lang-text");

  // Toggle dropdown
  if(languageSelector) {
    languageSelector.addEventListener("click", () => {
      dropdownMenu.classList.toggle("hidden");
    });
  }

  // Close dropdown when clicking outside
  document.addEventListener("click", (e) => {
    if (languageSelector && !languageSelector.contains(e.target)) {
      dropdownMenu.classList.add("hidden");
    }
  });

  // Set Language
  const setLanguage = (lang) => {
    // Update active text in selector
    const activeOption = document.querySelector(`.lang-option[data-lang="${lang}"]`);
    if (activeOption && currentLangText) {
      currentLangText.textContent = activeOption.textContent.trim();
    }

    // Update translations
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
      const key = el.getAttribute("data-i18n");
      if (translations[lang] && translations[lang][key]) {
        if (el.tagName === "INPUT" && el.type === "submit") {
          el.value = translations[lang][key];
        } else {
          el.textContent = translations[lang][key];
        }
      }
    });

    // Save to localStorage
    localStorage.setItem("arevatun_lang", lang);
    if(dropdownMenu) dropdownMenu.classList.add("hidden");
  };

  // Event Listeners for language options
  languageOptions.forEach(option => {
    option.addEventListener("click", (e) => {
      e.preventDefault();
      const lang = option.getAttribute("data-lang");
      setLanguage(lang);
    });
  });

  // Init Language
  const savedLang = localStorage.getItem("arevatun_lang") || "en";
  setLanguage(savedLang);

  // Lightbox Logic
  const lightbox = document.getElementById("lightbox");
  const lightboxImg = document.getElementById("lightbox-img");
  const closeLightbox = document.getElementById("close-lightbox");
  const galleryItems = document.querySelectorAll(".gallery-item img");

  if(lightbox && lightboxImg && closeLightbox) {
    galleryItems.forEach(item => {
      item.addEventListener("click", (e) => {
        lightboxImg.src = e.target.src;
        lightbox.classList.remove("hidden");
        lightbox.classList.add("flex");
        setTimeout(() => {
          lightbox.classList.remove("opacity-0");
        }, 10);
      });
    });

    const hideLightbox = () => {
      lightbox.classList.add("opacity-0");
      setTimeout(() => {
        lightbox.classList.add("hidden");
        lightbox.classList.remove("flex");
      }, 300);
    };

    closeLightbox.addEventListener("click", hideLightbox);
    
    lightbox.addEventListener("click", (e) => {
      if(e.target === lightbox) {
        hideLightbox();
      }
    });
  }

  // FAQ Accordion Logic
  const accordionButtons = document.querySelectorAll(".accordion-btn");
  accordionButtons.forEach(btn => {
    btn.addEventListener("click", () => {
      const content = btn.nextElementSibling;
      const icon = btn.querySelector('.accordion-icon');
      
      // Toggle current
      if(content.classList.contains("hidden")) {
        content.classList.remove("hidden");
        icon.style.transform = "rotate(180deg)";
      } else {
        content.classList.add("hidden");
        icon.style.transform = "rotate(0deg)";
      }
    });
  });
});
